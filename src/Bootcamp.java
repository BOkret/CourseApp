public class Bootcamp extends Course {
    private String trainerName;
    private String trainerSurname;
    private double consultingTime;

    public String getTrainerName() {
        return trainerName;
    }

    public void setTrainerName(String trainerName) {
        this.trainerName = trainerName;
    }

    public String getTrainerSurname() {
        return trainerSurname;
    }

    public void setTrainerSurname(String trainerSurname) {
        this.trainerSurname = trainerSurname;
    }

    public double getConsultingTime() {
        return consultingTime;
    }

    public void setConsultingTime(double consultingTime) {
        this.consultingTime = consultingTime;
    }
}
